import Button from "../components/Button"

export default function Game1() {
  const [score, setScore]= useState(0);
  const [scene, setScene]= useState(0);
  const scoreToWin = 2;

  function increaseScore(){
    // Add point to score
    setScore(score + 1);
    if (score == scoreToWin - 1) {
      setScene(3);
    }
  };

  function changeScene(){
    setScene(scene + 1);
  }

  <Head>
  <title>Game 1</title>
  <meta name="description" content="Generated by create next app" />
  <meta name="viewport" content="width=device-width, initial-scale=1.0" />
  <link rel="icon" href="/favicon.ico" />
  </Head>

  switch (scene) {
    case 1:
    return (
      <div className='relative'>
      <Paint itemClick={increaseScore} class="max-w-none " src="/jour_des_morts.jpg"/>
      <Score score={score}/>
      </div>
      )
      case 2:
      return (
        <div className='relative'>
        <Paint itemClick={increaseScore} class="max-w-none " src="/jour_des_morts.jpg"/>
        <Score score={score}/>
        </div>
        )
        case 3:
        return (
          <div className='relative'>
          <Paint itemClick={increaseScore} class="max-w-none " src="/jour_des_morts.jpg"/>
          <Score score={score}/>
          <h1 className='text-primary text-6xl font-bold fixed top-0 z-50'>GG Alexis, tu as une vue de lynx</h1>
          <div className='fixed bottom-8 right-0 z-50'>
            <Link href="/game2"><Button text="SUIVANT"/></Link>
          </div>
          </div>
          )
        default:
        return (
          <div className='relative overflow-hidden'>
            <img className='relative  h-screen max-w-none' src="/game1jaune.jpg" alt="" />
            <div className='w-screen h-screen fixed top-0 flex flex-col justify-center items-center'>
              <MessageBox text="Vous pouvez commencer la partie, essayez de trouver les deux queus de diable"/>
                <div className='fixed bottom-8 z-50'>
                  <Button text="Jouer" onClick={changeScene}/>
                </div>
              <MessageBox/>
            </div>
          </div>
          )
          }

        }
